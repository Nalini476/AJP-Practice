public class Bitwise {
    public static void main(String args[])
     {
        int num1=10;
        int num2=12;
        System.out.println(num1&num2); //1010 & 1100=>1000=8
        System.out.println(num1|num2);//1110=14
        System.out.println(num1^num2);//0110=6
        System.out.println(num1=num2);
    }
}
