public class Circle {
     static float pi =3.14f;
	 static float Area(float radius) {
		 return pi*radius*radius;
		}
	       public static void main(String[] args) {
	    	   float radius=3.5f;
	    	   float area=Area(radius);
	    	   System.out.println("area of the circle:"+area);
	
	

	}

}

